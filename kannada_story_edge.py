# -------------------------------------------------
# GENERATE SINGLE SCENE
# -------------------------------------------------
async def generate_scene(scene_name, text):
    output_path = f"outputs/{scene_name}.wav"
    print(f"üéôÔ∏è Processing {scene_name}...")

    communicate = edge_tts.Communicate(
        text=text,
        voice=VOICE,
        rate=RATE,
        pitch=PITCH
    )

    await communicate.save(output_path)
    print(f"‚úÖ Saved: {output_path}")

# -------------------------------------------------
# MAIN FUNCTION
# -------------------------------------------------
async def main():
    print(f"\nüîä Generating audio for {len(scenes)} scenes")
    print(f"Voice : {VOICE}")
    print(f"Rate  : {RATE}")
    print(f"Pitch : {PITCH}\n")

    for scene_name, text in scenes.items():
        await generate_scene(scene_name, text)

    print("\nüéâ All scenes generated successfully!")
    print("üìÅ Output folder: outputs/")

# -------------------------------------------------
# RUN
# -------------------------------------------------
if __name__ == "__main__":
    asyncio.run(main())
